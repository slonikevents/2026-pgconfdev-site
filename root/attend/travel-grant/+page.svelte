<h1>Travel Grant Program</h1>

<p>
  This program seeks to make PGConf.dev attendance affordable for those who
  can’t easily pay to travel and attend. By default, grants includes a
  complimentary registration and a hotel room in the conference affiliated hotel
  block. In some circumstances, we may be able to subsidize flights.
</p>

<h2>Eligibility Criteria</h2>
<p>
  Grant recipients should be those whose employers can’t or won’t pay for them
  to attend and be find it financially difficult to attend without a travel
  grant. Grant recipients should fall into one of the following categories:
</p>
<ol>
  <li>Speakers with an accepted session</li>

  <li>
    People who play an important role in the community (e.g. maintainer of an
    important ecosystem project
  </li>
  <li>
    People who play an important role in organizing the conference (e.g
    organizing committee member)
  </li>
  <li>
    People identified as up-and-coming hackers or community organizers who the
    community wants to sponsor and encourage to become more involved
  </li>
</ol>

